namespace Gwen.Net.New;

/// <summary>
/// Defines the vertical alignment of a visual within its parent.
/// </summary>
public enum VerticalAlignment
{
    /// <summary>
    /// The visual is stretched vertically to fill the available space within its parent.
    /// </summary>
    Stretch,
    
    /// <summary>
    /// The visual is aligned to the top edge of its parent.
    /// </summary>
    Top,

    /// <summary>
    /// The visual is centered vertically within its parent.
    /// </summary>
    Center,

    /// <summary>
    /// The visual is aligned to the bottom edge of its parent.
    /// </summary>
    Bottom
}
